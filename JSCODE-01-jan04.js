// Declare a string
let myString = "Hello, world!";


// Declare a boolean
let myBoolean = true;

// Declare null and undefined
let myNull = null;
let myUndefined = undefined;

// Use the typeof operator to determine the data type of a variable
console.log(typeof myNumber); // Output: "number"
console.log(typeof myString); // Output: "string"
console.log(typeof myBoolean); // Output: "boolean"
console.log(typeof myNull); // Output: "object"
console.log(typeof myUndefined); // Output: "undefined"

// Use arithmetic operators
let sum = 1 + 2; // Addition
let difference = 5 - 3; // Subtraction
let product = 4 * 6; // Multiplication
let quotient = 10 / 2; // Division
let remainder = 7 % 3; // Modulo

// Use assignment operators
let x = 1;
x += 2; // Equivalent to x = x + 2
x -= 1; // Equivalent to x = x - 1
x *= 3; // Equivalent to x = x * 3
x /= 2; // Equivalent to x = x / 2
x %= 4; // Equivalent to x = x % 4

// Use comparison operators
console.log(1 == "1"); // Output: true
console.log(1 === "1"); // Output: false
console.log(1 != "1"); // Output: false
console.log(1 !== "1"); // Output: true
console.log(1 < 2); // Output: true
console.log(1 > 2); // Output: false
console.log(1 <= 2); // Output: true
console.log(1 >= 2); // Output: false

// Use logical operators
console.log(true && false); // Output: false
console.log(true || false); // Output: true
console.log(!true); // Output: false

// Use bitwise operators
console.log(5 & 3); // Output: 1
console.log(5 | 3); // Output: 7
console.log(5 ^ 3); // Output: 6
console.log(~5); // Output: -6
console.log(5 << 1); // Output: 10
console.log(5 >> 1); // Output: 2

